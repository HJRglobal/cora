from typing import Any
from unittest.mock import MagicMock, patch

import pytest

_REPO_ROOT = Path(__file__).resolve().parents[1]
sys.path.insert(0, str(_REPO_ROOT / "src"))

from cora.tools import hubspot_client as h  # noqa: E402


def _corrupt(s: str) -> str:
    """Replay the migration corruption on a clean string (UTF-8 bytes read as cp1252)."""
    return s.encode("utf-8").decode("cp1252")


# ---------------------------------------------------------------------------
# Class 1: _build_mojibake_map — keys are genuine corruption sequences
# ---------------------------------------------------------------------------

class TestMojibakeMap:
    def test_em_dash_key_is_real_corruption(self):
        # Em-dash U+2014 -> bytes E2 80 94 -> cp1252 "â€”"
        em_corrupt = _corrupt("—")
        assert em_corrupt in h._MOJIBAKE_FIXES
        assert h._MOJIBAKE_FIXES[em_corrupt] == "—"

    def test_no_key_maps_to_itself(self):
        for bad, good in h._MOJIBAKE_FIXES.items():
            assert bad != good

    def test_no_bare_two_char_prefix_key(self):
        # A 2-char "â€" key would corrupt the longer 3-char keys on replace.
        assert _corrupt("—")[:2] not in h._MOJIBAKE_FIXES

    def test_all_keys_distinct(self):
        keys = list(h._MOJIBAKE_FIXES)
        assert len(keys) == len(set(keys))

    def test_keys_share_prefix_but_differ_in_third_char(self):
        # All keys start with the same 2 chars (â€) but the 3rd char disambiguates.
        thirds = {k[2] for k in h._MOJIBAKE_FIXES if len(k) >= 3}
        assert len(thirds) == len(h._MOJIBAKE_FIXES)


