class TestFixDealName:
    def test_repairs_em_dash(self):
        corrupt = "American Discount Foods " + _corrupt("—") + " Initial Order"
        assert h.fix_deal_name(corrupt) == "American Discount Foods — Initial Order"

    def test_repairs_en_dash(self):
        corrupt = "A " + _corrupt("–") + " B"
        assert h.fix_deal_name(corrupt) == "A – B"

    def test_repairs_apostrophe(self):
        corrupt = "Jesse" + _corrupt("’") + "s Mart"
        assert h.fix_deal_name(corrupt) == "Jesse’s Mart"

    def test_idempotent_on_clean_em_dash(self):
        clean = "Sprouts — F3E Retail Chain"
        assert h.fix_deal_name(clean) == clean

    def test_idempotent_double_apply(self):
        corrupt = "X " + _corrupt("—") + " Y"
        once = h.fix_deal_name(corrupt)
        assert h.fix_deal_name(once) == once

    def test_plain_ascii_unchanged(self):
        assert h.fix_deal_name("Whole Foods - Retail") == "Whole Foods - Retail"

    def test_empty_string(self):
        assert h.fix_deal_name("") == ""

    def test_none(self):
        assert h.fix_deal_name(None) is None

    def test_multiple_corruptions_in_one_name(self):
        corrupt = _corrupt("’") + "Test" + _corrupt("—") + "End"
        assert h.fix_deal_name(corrupt) == "’Test—End"


