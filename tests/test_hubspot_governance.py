def _mock_client(responses: list[dict[str, Any]] | None = None, status: int = 200, text: str = "err"):
    """Build a context-manager httpx.Client mock whose .post returns the given pages."""
    mc = MagicMock()
    mc.__enter__ = lambda s: mc
    mc.__exit__ = MagicMock(return_value=False)
    if responses is not None:
        idx = {"i": 0}

        def _post(*a, **k):
            r = MagicMock()
            r.status_code = 200
            r.json.return_value = responses[idx["i"]]
            idx["i"] += 1
            return r

        mc.post.side_effect = _post
    else:
        r = MagicMock()
        r.status_code = status
        r.text = text
        r.json.return_value = {"results": []}
        mc.post.return_value = r
    return mc


class TestPagedDealSearch:
    def test_single_page(self):
        page = {"results": [{"id": "1", "properties": {}}]}
        with patch("cora.tools.hubspot_client._token", return_value="t"):
            with patch("httpx.Client", return_value=_mock_client([page])):
                out = h._paged_deal_search([{"filters": []}], ["dealname"])
        assert len(out) == 1

    def test_follows_pagination(self):
        page1 = {"results": [{"id": "1", "properties": {}}], "paging": {"next": {"after": "c2"}}}
        page2 = {"results": [{"id": "2", "properties": {}}]}
        with patch("cora.tools.hubspot_client._token", return_value="t"):
            with patch("httpx.Client", return_value=_mock_client([page1, page2])):
                out = h._paged_deal_search([{"filters": []}], ["dealname"])
        assert {d["id"] for d in out} == {"1", "2"}

    def test_respects_max_deals(self):
        page1 = {"results": [{"id": "1", "properties": {}}, {"id": "2", "properties": {}}],
                 "paging": {"next": {"after": "c2"}}}
        with patch("cora.tools.hubspot_client._token", return_value="t"):
            with patch("httpx.Client", return_value=_mock_client([page1])):
                out = h._paged_deal_search([{"filters": []}], ["dealname"], max_deals=1)
        assert len(out) == 1

    def test_401_raises(self):
        with patch("cora.tools.hubspot_client._token", return_value="t"):
            with patch("httpx.Client", return_value=_mock_client(status=401)):
                with pytest.raises(h.HubSpotClientError, match="401"):
                    h._paged_deal_search([{"filters": []}], ["dealname"])

    def test_500_raises(self):
        with patch("cora.tools.hubspot_client._token", return_value="t"):
            with patch("httpx.Client", return_value=_mock_client(status=500)):
                with pytest.raises(h.HubSpotClientError):
                    h._paged_deal_search([{"filters": []}], ["dealname"])

    def test_network_error_raises(self):
        import httpx
        mc = MagicMock()
        mc.__enter__ = lambda s: mc
        mc.__exit__ = MagicMock(return_value=False)
        mc.post.side_effect = httpx.RequestError("boom")
        with patch("cora.tools.hubspot_client._token", return_value="t"):
            with patch("httpx.Client", return_value=mc):
                with pytest.raises(h.HubSpotClientError, match="network error"):
                    h._paged_deal_search([{"filters": []}], ["dealname"])


