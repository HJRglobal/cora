_STAGE_CACHE = {
    "3760235201": "Identify",
    "3760235204": "Qualified",
    "3760235206": "Closed Won",
    "3760235207": "Closed Lost",
}


class TestGetDealsByFilter:
    def _deal(self, did, stage, **props):
        return {"id": did, "properties": {"dealstage": stage, **props}}

    def test_builds_all_filters(self):
        captured = {}

        def _fake_search(filter_groups, properties, max_deals=200):
            captured["fg"] = filter_groups
            return []

        with patch("cora.tools.hubspot_client._STAGE_NAME_CACHE", _STAGE_CACHE):
            with patch("cora.tools.hubspot_client._paged_deal_search", side_effect=_fake_search):
                h.get_deals_by_filter(entity="F3E", direction="revenue_in",
                                      owner_id="162944825", stage_id="3760235204",
                                      pipeline_id="2313722582")
        filters = captured["fg"][0]["filters"]
        names = {f["propertyName"]: f["value"] for f in filters}
        assert names["f3_entity"] == "F3E"
        assert names["f3_deal_direction"] == "revenue_in"
        assert names["hubspot_owner_id"] == "162944825"
        assert names["dealstage"] == "3760235204"
        assert names["pipeline"] == "2313722582"

    def test_no_filters_sends_empty_group(self):
        captured = {}
        with patch("cora.tools.hubspot_client._STAGE_NAME_CACHE", _STAGE_CACHE):
            with patch("cora.tools.hubspot_client._paged_deal_search",
                       side_effect=lambda fg, p, max_deals=200: captured.setdefault("fg", fg) or []):
                h.get_deals_by_filter()
        assert captured["fg"] == [{"filters": []}]

    def test_drops_closed_by_default(self):
        deals = [self._deal("1", "3760235204"), self._deal("2", "3760235206"),
                 self._deal("3", "3760235207")]
        with patch("cora.tools.hubspot_client._STAGE_NAME_CACHE", _STAGE_CACHE):
            with patch("cora.tools.hubspot_client._paged_deal_search", return_value=deals):
                out = h.get_deals_by_filter(entity="F3E")
        ids = {d["id"] for d in out}
        assert ids == {"1"}

    def test_include_closed_keeps_all(self):
        deals = [self._deal("1", "3760235204"), self._deal("2", "3760235206")]
        with patch("cora.tools.hubspot_client._STAGE_NAME_CACHE", _STAGE_CACHE):
            with patch("cora.tools.hubspot_client._paged_deal_search", return_value=deals):
                out = h.get_deals_by_filter(entity="F3E", include_closed=True)
        assert len(out) == 2

    def test_refreshes_cache_when_empty(self):
        with patch("cora.tools.hubspot_client._STAGE_NAME_CACHE", {}):
            with patch("cora.tools.hubspot_client._refresh_pipeline_cache") as refresh:
                with patch("cora.tools.hubspot_client._paged_deal_search", return_value=[]):
                    h.get_deals_by_filter(entity="F3E")
        refresh.assert_called_once()


