class TestGetUntaggedDeals:
    def test_or_groups_for_three_props(self):
        captured = {}
        with patch("cora.tools.hubspot_client._paged_deal_search",
                   side_effect=lambda fg, p, max_deals=200: captured.setdefault("fg", fg) or []):
            h.get_untagged_deals()
        fg = captured["fg"]
        # One filter group per governance prop (OR semantics), each NOT_HAS_PROPERTY
        assert len(fg) == 3
        props = {g["filters"][0]["propertyName"] for g in fg}
        assert props == {"f3_entity", "f3_deal_direction", "hjr_deal_type"}
        for g in fg:
            assert g["filters"][0]["operator"] == "NOT_HAS_PROPERTY"

    def test_returns_results(self):
        with patch("cora.tools.hubspot_client._paged_deal_search",
                   return_value=[{"id": "9", "properties": {}}]):
            out = h.get_untagged_deals()
        assert out and out[0]["id"] == "9"


