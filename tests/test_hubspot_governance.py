"""Tests for the 2026-06-08 HubSpot governance helpers in hubspot_client.

Coverage:
  - fix_deal_name(): mojibake repair (em-dash et al.), idempotency, edge cases
  - _build_mojibake_map(): keys are the genuine cp1252 mis-decode sequences
  - _paged_deal_search(): pagination + error handling
  - get_deals_by_filter(): filter construction, closed-stage drop, include_closed
  - get_untagged_deals(): OR filter groups across the 3 governance props

Mojibake strings are GENERATED by replaying the UTF-8->cp1252 corruption
(``ch.encode("utf-8").decode("cp1252")``) rather than hand-typed, because
transport layers silently normalize raw mojibake back to clean characters.
"""

